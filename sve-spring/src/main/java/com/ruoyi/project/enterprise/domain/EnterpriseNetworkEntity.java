package com.ruoyi.project.enterprise.domain;

public class EnterpriseNetworkEntity {

    private String fullName;
    private String socialCreditCode;
    private String townName;
    private Integer townCode;
    private String shortName;
    private Integer status;
    private String statusName;
    private String mcnt;
    private String hcnt;
    private String dcnt;

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getSocialCreditCode() {
        return socialCreditCode;
    }

    public void setSocialCreditCode(String socialCreditCode) {
        this.socialCreditCode = socialCreditCode;
    }

    public String getTownName() {
        return townName;
    }

    public void setTownName(String townName) {
        this.townName = townName;
    }

    public Integer getTownCode() {
        return townCode;
    }

    public void setTownCode(Integer townCode) {
        this.townCode = townCode;
    }

    public String getShortName() {
        return shortName;
    }

    public void setShortName(String shortName) {
        this.shortName = shortName;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public String getMcnt() {
        return mcnt;
    }

    public void setMcnt(String mcnt) {
        this.mcnt = mcnt;
    }

    public String getHcnt() {
        return hcnt;
    }

    public void setHcnt(String hcnt) {
        this.hcnt = hcnt;
    }

    public String getDcnt() {
        return dcnt;
    }

    public void setDcnt(String dcnt) {
        this.dcnt = dcnt;
    }

    public String getStatusName() {
        return statusName;
    }

    public void setStatusName(String statusName) {
        this.statusName = statusName;
    }
}
